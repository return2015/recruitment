package com.returnsoft.resource.exception;

public class RecruitmentException extends Exception {

	private static final long serialVersionUID = 1L;

	public RecruitmentException() {
		super();
	}

	public RecruitmentException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

	public RecruitmentException(String arg0) {
		super(arg0);
	}

	public RecruitmentException(Throwable arg0) {
		super(arg0);
	}	
}
